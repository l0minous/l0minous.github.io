function BitmapIcon({ bitmap, fg, bg }: { bitmap: number[][]; fg: string; bg: string }) {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext('2d')!;
    const imageData = ctx.createImageData(16, 16);

    const fgR = parseInt(fg.slice(1, 3), 16);
    const fgG = parseInt(fg.slice(3, 5), 16);
    const fgB = parseInt(fg.slice(5, 7), 16);
    const bgR = parseInt(bg.slice(1, 3), 16);
    const bgG = parseInt(bg.slice(3, 5), 16);
    const bgB = parseInt(bg.slice(5, 7), 16);

    for (let y = 0; y < 16; y++) {
      for (let x = 0; x < 16; x++) {
        const i = (y * 16 + x) * 4;
        const on = bitmap[y][x];
        imageData.data[i]     = on ? fgR : bgR;
        imageData.data[i + 1] = on ? fgG : bgG;
        imageData.data[i + 2] = on ? fgB : bgB;
        imageData.data[i + 3] = 255;
      }
    }
    ctx.putImageData(imageData, 0, 0);
  }, [bitmap, fg, bg]);

  return (
    <canvas
      ref={canvasRef}
      width={16}
      height={16}
      style={{
        width: '28px',
        height: '28px',
        imageRendering: 'pixelated',
        display: 'block',
      }}
    />
  );
}

